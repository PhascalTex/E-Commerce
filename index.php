<script>
  $(document).ready(function(){
    $(document).on('submit', '#userForm', function(e){
      e.preventDefault();
      $.ajax({
          url: 'process.php',
          type: 'POST',
          data: $(this).serialize(),
          dataType: 'json',
          success: function(response){
              console.log(response);
            if(!response.status){
              if(response.errors.email){
                $('#errorEmail').text(response.errors.email);
              }else{
                $('#errorEmail').text('');
              }
              if(response.errors.itemName){
                $('#errorItem').text(response.errors.itemName);
              }else{
                $('#errorItem').text('');
              }
              if(response.errors.qty){
                $('#errorQty').text(response.errors.qty)
              }else{
                $('#errorQty').text('');
              }
              if(response.errors.price){
                $('#errorPrice').text(response.errors.price);
              }else{
                $('#errorPrice').text('');
              }
            }else{
              const Toast = Swal.mixin({
              toast: true,
              position: "top-end",
              showConfirmButton: false,
              timer: 3000,
              timerProgressBar: true,
              didOpen: (toast) => {
                toast.onmouseenter = Swal.stopTimer;
                toast.onmouseleave = Swal.resumeTimer;
              }
            });
            Toast.fire({
              icon: "success",
              title: response.message.success
            });
            $('#exampleModal').modal('hide');
            $('#userForm')[0].reset();
            $('#errorEmail').text('');
            $('#errorQty').text('');
          }
          },
          error: function(xhr, status, error){
              console.error('Error' + xhr.responseText)
              alert('error script:' + error);
              console.error(error);
          }
        });
    });
  });
</script>
